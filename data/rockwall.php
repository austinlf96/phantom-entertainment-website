    function replaceCard(event) {
        switch(event.target.id){
            case 'hard':
                document.getElementById('title').innerHTML = 'Hard Rock Wall';
                document.getElementById('summary').innerHTML = 'This rock wall is designed to feel just like the real thing! Don\'t worry if your foot slips though, because the Hard Rock Wall will catch you too!';
                break;
            case 'face':
                document.getElementById('title').innerHTML = 'Face To Face Rock Wall';
                document.getElementById('summary').innerHTML = 'Two participants will clash head to head in an epic race to the finish line. Who will be the first to press the button?!';
                break;
            case 'volcano':
                document.getElementById('title').innerHTML = 'Volcano Inflatable Rock Wall';
                document.getElementById('summary').innerHTML = 'Climb quickly to get a view from the top of the volcano, before it erupts! <i>Real active volcano not included and not available for rental.</i>';
                break;
            default:
                document.getElementById('title').innerHTML = "Hard Rock Wall";
                document.getElementById('summary').innerHTML = 'This rock wall is designed to feel just like the real thing! Don\'t worry if your foot slips though, because the Hard Rock Wall will catch you too!';
                break;
        }
        document.getElementById('container').innerHTML = cards[event.target.id];
    }
    elements = document.getElementsByClassName('card-button');
