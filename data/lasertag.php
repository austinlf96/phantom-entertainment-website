    function replaceCard(event) {
        switch(event.target.id){
            case 'extreme':
                document.getElementById('title').innerHTML = 'Extreme Laser Tag';
                break;
            case 'battledome':
                document.getElementById('title').innerHTML = 'Battle Dome Laser Tag';
                break;
