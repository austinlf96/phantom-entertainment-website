    };
    function replaceCard(event) {
        switch(event.target.id){
            case 'quarterback':
                document.getElementById('title').innerHTML = 'Armchair Quarterback';
                break;
            case 'basketball':
                document.getElementById('title').innerHTML = 'Armchair Basketball';
                break;
            case 'baseball':
                document.getElementById('title').innerHTML = 'Armchair Baseball';
                break;
            default:
                document.getElementById('title').innerHTML = "Armchair Sports Rides";
                break;
        }
        document.getElementById('container').innerHTML = armchairCards[event.target.id]
    }
    elements = document.getElementsByClassName('card-button');
    for(let i = 0; i < elements.length; i++){
        elements[i].addEventListener("click", replaceCard)
    }
